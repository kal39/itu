static long itu_calculate_color(itu_Character character, itu_Image image, int x, int y, itu_Color *fgColor,
								itu_Color *bgColor) {
	// don't use itu_Color because it uses unsigned chars to store rgb values; thus overflows
	int fgColorArr[3] = {0, 0, 0}, bgColorArr[3] = {0, 0, 0};
	int fgColorCount = 0, bgColorCount = 0, error = 0;

	for (int i = 0; i < CHARACTER_HEIGHT; i++) {
		for (int j = 0; j < CHARACTER_WIDTH; j++) {
			int pos_x = (int)((float)(j + x * CHARACTER_WIDTH) / image.scale_x);
			int pos_y = (int)((float)(i + y * CHARACTER_HEIGHT) / image.scale_y);
			int idx = (pos_y * image.width + pos_x) * 3;

			// colorArr and colorCount point to either fg or bg variables
			int(*colorArr)[3] = character.pixels[i * CHARACTER_WIDTH + j] ? &fgColorArr : &bgColorArr;
			int *colorCount = character.pixels[i * CHARACTER_WIDTH + j] ? &fgColorCount : &bgColorCount;

			(*colorArr)[0] += image.data[idx + 0];
			(*colorArr)[1] += image.data[idx + 1];
			(*colorArr)[2] += image.data[idx + 2];
			(*colorCount)++;

			error += abs((*colorArr)[0] / *colorCount - image.data[idx + 0]);
			error += abs((*colorArr)[1] / *colorCount - image.data[idx + 1]);
			error += abs((*colorArr)[2] / *colorCount - image.data[idx + 2]);
		}
	}

	// assign to itu_Color after dividing to avoid overflow
	fgColor->r = fgColorArr[0] / fgColorCount;
	fgColor->g = fgColorArr[1] / fgColorCount;
	fgColor->b = fgColorArr[2] / fgColorCount;

	bgColor->r = bgColorArr[0] / bgColorCount;
	bgColor->g = bgColorArr[1] / bgColorCount;
	bgColor->b = bgColorArr[2] / bgColorCount;

	return error;
}

static void itu_to_string(char *out, itu_Character character, itu_Color fgColor, itu_Color bgColor) {
	// use leading 0s to keep string length consistent
	sprintf(out,
			"\x1b[38;2;%03d;%03d;%03dm\x1b[48;2;%03d;%03d;%03dm%s",
			fgColor.r,
			fgColor.g,
			fgColor.b,
			bgColor.r,
			bgColor.g,
			bgColor.b,
			character.character);
}

static void itu_convert_image_area(char *out, itu_Image image, int x, int y, int detail) {
	int min = INT_MAX;
	itu_Character minChar;
	itu_Color minFgColor, minBgColor;

	for (int i = 0; i < CUTOFF_POINTS[detail]; i++) {
		itu_Color fgColor, bgColor;
		int error = itu_calculate_color(ALLOWED_CHARACTERS[i], image, x, y, &fgColor, &bgColor);

		if (error < min) {
			min = error;
			minChar = ALLOWED_CHARACTERS[i];
			minFgColor = fgColor;
			minBgColor = bgColor;
		}
	}

	itu_to_string(out, minChar, minFgColor, minBgColor);
}

static void *itu_convert_row_threaded(void *_args) {
	itu_ThreadArgs *args = (itu_ThreadArgs *)_args;

	for (int j = 0; j < args->out_width; j++) {
		long offset = CHARACTER_STRLEN * ((args->y * args->out_width) + j) + args->y;
		itu_convert_image_area(args->out + offset, args->image, j, args->y, args->detail);
	}

	// newline
	*(args->out + CHARACTER_STRLEN * (args->y + 1) * args->out_width + args->y) = '\n';
}

char *itu_allocate_string(int out_width, int out_height) {
	if (out_width < 0 || out_height < 0) return NULL;

	// allocate space for charaters, newlines, color reset, and \0
	int strLen = CHARACTER_STRLEN * out_width * out_height + (out_height - 1) + 4 + 1;
	return malloc(strLen);
}

int itu_convert_image(char *out, unsigned char *data, int in_width, int in_height, int out_width, int out_height,
					  int detail) {
	if (in_width < 0 || in_height < 0 || out_width < 0 || out_height < 0) return -1;
	detail = detail < 0 ? 0 : detail > CUTOFF_POINT_COUNT - 1 ? CUTOFF_POINT_COUNT - 1 : detail;

	float scale_x = (float)out_width * (float)CHARACTER_WIDTH / (float)in_width;
	float scale_y = (float)out_height * (float)CHARACTER_HEIGHT / (float)in_height;

	itu_Image image = (itu_Image){data, in_width, in_height, scale_x, scale_y};

	itu_ThreadArgs *args = malloc(sizeof(itu_ThreadArgs) * out_height);
	pthread_t *threads = malloc(sizeof(pthread_t) * out_height);

	if (!args || !threads) return -2;

	for (int i = 0; i < out_height; i++) {
		args[i] = (itu_ThreadArgs){
			out_width,
			out,
			image,
			i,
			detail,
		};

		pthread_create(&threads[i], NULL, itu_convert_row_threaded, &args[i]);
	}

	// wait for threads to finish
	for (int i = 0; i < out_height; i++) pthread_join(threads[i], NULL);

	free(threads);
	free(args);

	// reset colors
	long offset = CHARACTER_STRLEN * out_width * out_height + (out_height - 1);
	sprintf(out + offset, "\e[0m");

	return 0;
}

